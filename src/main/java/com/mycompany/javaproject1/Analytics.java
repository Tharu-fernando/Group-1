    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed


        // Based on your UI: Field2=Water, Field3=Soil, Field1=Biodiversity
        String waterStr = jTextField2.getText(); 
        String soilStr = jTextField3.getText();  
        String bioStr = jTextField1.getText();   

        if (waterStr.isEmpty() || soilStr.isEmpty() || bioStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields!");
            return;
        }

        try {
            int water = Integer.parseInt(waterStr);
            int soil = Integer.parseInt(soilStr);
            int bio = Integer.parseInt(bioStr);

            // MOCK CALCULATION: Sum divided by 2
            int carbonReduction = (water + soil + bio) / 2;

            // Save to DB
            Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
            // Note: 'carbon reduction' has a space, so use backticks ` `
            String sql = "INSERT INTO carbon (water, soil, biodiversity, `carbon reduction`) VALUES (?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, water);
            pst.setInt(2, soil);
            pst.setInt(3, bio);
            pst.setInt(4, carbonReduction);

            int rows = pst.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Data Added! Reduction: " + carbonReduction);
                // Clear fields
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                // Refresh Table
                loadCarbonData();
            }
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

