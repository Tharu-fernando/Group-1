    // 2. Method to Load Data
    private void loadCarbonData() {
        try {
            Connection con = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);
            Statement stmt = con.createStatement();
            // Using backticks ` ` because the column name has a space
            String sql = "SELECT * FROM carbon"; 
            ResultSet rs = stmt.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) jTablel.getModel();
            model.setRowCount(0); // Clear table

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getInt("water"));
                v.add(rs.getInt("soil"));
                v.add(rs.getInt("biodiversity"));
                v.add(rs.getInt("carbon reduction")); 
                model.addRow(v);
            }
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
